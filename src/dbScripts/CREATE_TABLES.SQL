CREATE TABLE KITCHENWARE (
	kitchenware_id BIGSERIAL NOT NULL PRIMARY KEY,
	name VARCHAR(256) NOT NULL UNIQUE ,
	kitchenware_category_id INTEGER,
	photo_id BIGINT,
	is_active BOOLEAN NOT NULL
);


CREATE TABLE EVENT (
	event_id BIGSERIAL NOT NULL PRIMARY KEY,
	name VARCHAR(256) NOT NULL,
	description VARCHAR(2000),
	date DATE,
	place VARCHAR(256),
	creator_id BIGINT
);


CREATE TABLE INGRIDIENT (
	ingridient_id BIGSERIAL NOT NULL PRIMARY KEY,
	name VARCHAR(256) NOT NULL UNIQUE ,
	ingridient_category_id BIGINT,
	photo_id BIGINT,
	is_active BOOLEAN NOT NULL
);


CREATE TABLE DISH (
	dish_id BIGSERIAL NOT NULL PRIMARY KEY,
	name VARCHAR(256) UNIQUE NOT NULL,
	description VARCHAR(2000),
	dish_type VARCHAR(256),
	dish_category_id BIGINT,
	label VARCHAR(256),
	receipt VARCHAR(2000) NOT NULL,
	likes BIGINT NOT NULL,
	is_active BOOLEAN NOT NULL
);


CREATE TABLE PERSON (
	person_id BIGSERIAL NOT NULL PRIMARY KEY,
	name VARCHAR(256) NOT NULL,
	email VARCHAR(256) NOT NULL UNIQUE,
	password VARCHAR(256) NOT NULL,
	photo_id BIGINT,
	blog_id BIGINT,
	role_id INTEGER,
	is_active BOOLEAN NOT NULL
);


CREATE TABLE PERSON_IN_EVENT (
	person_in_event_id BIGSERIAL NOT NULL PRIMARY KEY,
	event_id BIGINT,
	person_id BIGINT
);


CREATE TABLE BLOG (
	blog_id BIGSERIAL NOT NULL PRIMARY KEY,
	person_id BIGINT
);


CREATE TABLE STOCK (
	stock_id BIGSERIAL NOT NULL PRIMARY KEY,
	person_id BIGINT,
	ingridient_id BIGINT,
	amount INTEGER,
	measure_type VARCHAR(256)
);


CREATE TABLE RATE (
	rate_id BIGSERIAL NOT NULL PRIMARY KEY,
	comment VARCHAR(2000),
	rate REAL,
	person_id BIGINT,
	dish_id BIGINT
);


CREATE TABLE ROLE (
	role_id SERIAL NOT NULL PRIMARY KEY,
	name VARCHAR(256) NOT NULL
);


CREATE TABLE FRIENDSHIP (
	friendship_id BIGSERIAL NOT NULL PRIMARY KEY,
	person_id_reciever BIGINT,
	person_id_initiator BIGINT
);


CREATE TABLE POST (
	post_id BIGSERIAL NOT NULL PRIMARY KEY,
	name VARCHAR(256) NOT NULL,
	content TEXT NOT NULL,
	blog_id BIGINT
);


CREATE TABLE FAVOURITE_DISH (
	favourite_dish_id BIGSERIAL NOT NULL PRIMARY KEY,
	person_id BIGINT,
	dish_id BIGINT
);


CREATE TABLE DISHES_IN_EVENT (
	dishes_in_event_id BIGSERIAL NOT NULL PRIMARY KEY,
	event_id BIGINT,
	dish_id BIGINT,
	amount INTEGER
);


CREATE TABLE INGRIDIENT_DISH (
	ingridient_dish_id BIGSERIAL NOT NULL PRIMARY KEY,
	dish_id BIGINT,
	ingridient_id BIGINT,
	amount INTEGER,
	measure_type VARCHAR(256)
);


CREATE TABLE KITCHENWARE_DISH (
	kitchenware_dish_id BIGSERIAL NOT NULL PRIMARY KEY,
	dish_id BIGINT,
	kitchenware_id BIGINT
);


CREATE TABLE INGRIDIENT_CATEGORY (
	ingridient_category_id BIGSERIAL NOT NULL PRIMARY KEY,
	name VARCHAR(256)
);


CREATE TABLE KITCHENWARE_CATEGORY (
	kitchenware_category_id BIGSERIAL NOT NULL PRIMARY KEY,
	name VARCHAR(256)
);


CREATE TABLE DISH_CATEGORY (
	dish_category_id BIGSERIAL NOT NULL PRIMARY KEY,
	name VARCHAR(256)
);

CREATE TABLE PHOTO (
	photo_id BIGSERIAL NOT NULL PRIMARY KEY,
	url VARCHAR(256)
);

CREATE TABLE NOTIFICATION (
	notification_id BIGSERIAL NOT NULL PRIMARY KEY,
	person_id  BIGINT,
	event_id BIGINT,
	status VARCHAR(256)
);

CREATE TABLE PASSWORD_CHANGE_REQUEST (
         request_id BIGSERIAL NOT NULL PRIMARY KEY,
         person_id BIGINT NOT NULL,
         time TIMESTAMP,
         id VARCHAR(256) NOT NULL
);

CREATE TABLE LIKES (
         pair_id BIGSERIAL NOT NULL PRIMARY KEY,
         person_id BIGINT NOT NULL,
         dish_id BIGINT NOT NULL
);

CREATE TABLE FRIENDSHIP_INVITATION (
    friendship_id BIGSERIAL NOT NULL PRIMARY KEY,
    person_id_initiator BIGINT NOT NULL,
    person_id_receiver BIGINT NOT NULL,
    datetime TIMESTAMP,
    message VARCHAR(256)
);
